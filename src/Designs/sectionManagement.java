    private void findBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBActionPerformed
        // TODO add your handling code here:
        String key = courseidTF.getText();
        Course c = new Course();
        c = University.searchCourse(key);
        Section s = new Section("Name", "INFINITY", "SPACE", 1, new Teacher(), c);
        c.getSections().add(s);
        if(!c.getCourseId().isEmpty()){
            if(!c.getSections().isEmpty()){
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                model.setRowCount(0);
                for (int i = 0; i < c.getSections().size(); i++) {
                
                
                    Object [] cols = new Object[6];
                    cols[0] = c.getSection(i).getName();
                    cols[1] = c.getSection(i).getRoom();
                    cols[2] = c.getSection(i).getSlotNo();
                    cols[3] = c.getSection(i).getInstructor();
                    cols[4] = c.getSection(i).getDay();
                    cols[5] = c.getSection(i).getCourse().getName();
                    model.addRow(cols);
                }
            }
        }
        
    }//GEN-LAST:event_findBActionPerformed

