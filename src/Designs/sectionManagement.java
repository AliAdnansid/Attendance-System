    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed
        // TODO add your handling code here:
        String key = courseidTF.getText();
        Course c = new Course();
        c = University.searchCourse(key);
        if(!c.getCourseId().isEmpty()){
            String teacherName = teacherTF.getText();
            Teacher t = new Teacher();
            t = University.getTeacherByName(teacherName); 
            if(!t.getName().isEmpty()){
            Section e = new Section(nameTF.getText(), dayTF.getText(), roomTF.getText(), Integer.parseInt(slotnoTF.getText()), t, c);
            University.searchCourse(key).getSections().add(e);
            University.getSections().add(e);
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);
            
            Object [] cols = new Object[6];
            cols[0] = e.getName();
            cols[1] = e.getRoom();
            cols[2] = e.getSlotNo();
            cols[3] = e.getInstructor().getName();
            cols[4] = e.getDay();
            cols[5] = e.getCourse().getName();
            model.addRow(cols);
            
            }
        }
    }//GEN-LAST:event_addBActionPerformed

